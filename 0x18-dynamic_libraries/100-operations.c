int add(int a, int b);
int sub(int a, int b);
int mul(int a, int b);
int div(int a, int b);
int mod(int a, int b);
/**
 * add - returns the sum of a and b.
 * @a: number one.
 * @b: number two.
 * Return: a+b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - returns the difference of a and b
 * @a: number one.
 * @b: number two.
 * Return: a-b.
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - returns the product of a and b.
 * @a: number one.
 * @b: number two.
 * Return: a * b.
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - returns the result of the division of a by b.
 * @a: number one.
 * @b: number two.
 * Return: a/b.
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod -  returns the remainder of the division of a by b.
 * @a: number one.
 * @b: number two.
 * Return: a % b.
 */
int mod(int a, int b)
{
	return (a % b);
}

